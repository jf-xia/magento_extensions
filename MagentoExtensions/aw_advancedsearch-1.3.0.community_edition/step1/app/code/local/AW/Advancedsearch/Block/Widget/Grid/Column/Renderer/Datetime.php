class AW_Advancedsearch_Block_Widget_Grid_Column_Renderer_Datetime extends Mage_Adminhtml_Block_Widget_Grid_Column_Renderer_Datetime {
    public function render(Varien_Object $row) {
        $data = $row->getData($this->getColumn()->getIndex());
        return is_empty_date($data) ? $this->__('Never') : parent::render($row);
    }
}
